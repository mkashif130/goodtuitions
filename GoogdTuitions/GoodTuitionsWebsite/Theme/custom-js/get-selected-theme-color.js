function GetColor(css) {
    var def = '#00708c';
    if (css == 'color-default.css') {
        return "#00708c";
    }
    if (css == 'color-niceblue.css') {
        return '#008cba';
    }
    if (css == 'color-intenseblue.css') {
        return "#0093e8";
    }
    if (css == 'color-otherblue.css') {
        return '#429ab4';
    }
    if (css == 'color-blue.css') {
        return '#00439e';
    }
    if (css == 'color-puregreen.css') {
        return '#068100';
    }
    if (css == 'color-grassgreen.css') {
        return '#5a9900';
    }
    if (css == 'color-green.css') {
        return '#008c73';
    }
    if (css == 'color-olive.css') {
        return '#88a026';
    }
    if (css == 'color-gold.css') {
        return '#a79c42';
    }
    if (css == 'color-orange.css') {
        return '#d77400';
    }
    if (css == 'color-pink.css') {
        return '#db46e4';
    }
    if (css == 'color-fuchsia.css') {
        return "#cc0261";
    }
    if (css == 'color-violet.css') {
        return '#5400ab';
    }
    if (css == 'color-red.css') {
        return '#a00000';
    }
    return def;
}